// comparison operators -> true or false - boolean also called as relational operators

// >, < , >= , <=, ==, === , !=, !==, !===

// = -> assignment operator
// == -> equality operator (loose equality) -> compares values after type coercion
// === -> strict equality operator -> compares values without type coercion

console.log(10 > 5);
console.log(10 < 5);
console.log(7 >= 7); // 7 > 7 false, or 7 == 7 true
console.log(10 <= 6); // 

// 10 > 5 -> true
// 10 < 5 -> false
// 7 >= 7 -> true
// 10 <= 6 -> false
