// Pre Increment
let a = 10;
let b = ++a; // (increment the a by 1)
console.log("Preincrement : a & b", a, b);

//Pre Decrement
let d = 10;
let e = --d; // (decrement the a by 1)
console.log("Pre-decrement d & e :", d, e);

// Post decrement
let p = 10;
let q = p--;
console.log("Post Decrement p & q : ", p, q);

// Post increment
let r = 10;
let s = r++;
console.log("Post Decrement r & s : ", r, s);