// Storing the function to a variable and call it
// Functions as Expression

const greet = function (name) {
    return `Hello, ${name}!`;
}

let result = greet("Priya");
console.log(result);

// Type 4 - Normal Function
function greet1(name1) {
    return `Hi, ${name1}!`
}
let r = greet1("Priya1");
console.log(r);

