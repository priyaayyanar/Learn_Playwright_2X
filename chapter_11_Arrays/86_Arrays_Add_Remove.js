let arr = [1, 2, 3, 4, 5];
console.log(arr);

// Add to End
arr.push(6);
console.log(arr);

// Remove from end
arr.pop();
console.log(arr);

// Multiple Items/Elements can be pushed at a time
arr.push(6, 7);
console.log(arr);

// Add to begining
arr.unshift(0);
console.log(arr);

// Remove from begining
arr.shift();
console.log(arr);

arr.unshift(100);
console.log(arr);

arr.shift();
console.log(arr);


